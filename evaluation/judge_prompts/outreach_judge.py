# ---------------------------------------------------------------------------
# HELPERS
# ---------------------------------------------------------------------------

def _fmt_company_core(core: dict) -> str:
    if not core:
        return "  (no company_core data available)"
    lines = []
    for k, v in core.items():
        lines.append(f"  {k}: {v}")
    return "\n".join(lines)


def _fmt_founders(founders: list) -> str:
    if not founders:
        return "  (no founder data available)"
    blocks = []
    for f in founders:
        name = f.get("name", "Unknown")
        title = f.get("title", "")
        linkedin = f.get("linkedin_url", "")
        background = f.get("background", "")
        blocks.append(
            f"  — {name} | {title}\n"
            f"    LinkedIn: {linkedin or 'N/A'}\n"
            f"    Background: {background or 'Not available'}"
        )
    return "\n".join(blocks)


def _fmt_signals(signals: list) -> str:
    if not signals:
        return "  (no key signals available)"
    lines = []
    for s in signals:
        sig_type = s.get("signal_type", "unknown")
        desc = s.get("description", "")
        source = s.get("source", "")
        timestamp = s.get("timestamp", "")
        lines.append(f"  [{sig_type}] {desc}  (source: {source}, {timestamp})")
    return "\n".join(lines)


def _fmt_news(articles: list) -> str:
    if not articles:
        return "  (no news articles available)"
    lines = []
    for a in articles:
        headline = a.get("headline", a.get("title", ""))
        outlet = a.get("outlet", a.get("source", ""))
        date = a.get("published_date", a.get("date", ""))
        url = a.get("url", "")
        excerpt = a.get("excerpt", a.get("description", ""))
        lines.append(
            f"  Headline: {headline}\n"
            f"  Outlet: {outlet} | Date: {date}\n"
            f"  URL: {url}\n"
            f"  Excerpt: {excerpt}"
        )
    return "\n\n".join(lines)


# ---------------------------------------------------------------------------
# MAIN BUILDER
# ---------------------------------------------------------------------------

def build_judge_prompt(
    company_bundle: dict[str, Any],
    agent_output: dict[str, Any],
    context_tags: dict[str, str],
) -> tuple[str, str]:
    """
    Build the (system_prompt, user_prompt) pair for the Outreach Agent judge.

    Args:
        company_bundle: Dict with keys company_core, founders, key_signals,
                        news_articles — exactly what the agent received.
        agent_output:   Dict with keys subject, message, context_type,
                        investor_key, output_format, contact_name, etc.
        context_tags:   Dict with keys data_richness, feedback_state,
                        investor_context, investor, format.

    Returns:
        (system_prompt, user_prompt) strings for the Messages API.
    """
    investor = context_tags.get("investor", "unknown")
    fmt = context_tags.get("format", "Email")
    data_richness = context_tags.get("data_richness", "Unknown")
    feedback_state = context_tags.get("feedback_state", "Unknown")
    investor_context = context_tags.get("investor_context", "Unknown")

    company_core = company_bundle.get("company_core", {})
    founders = company_bundle.get("founders", [])
    key_signals = company_bundle.get("key_signals", [])
    news_articles = company_bundle.get("news_articles", [])

    subject_line = agent_output.get("subject", "")
    message = agent_output.get("message", "")
    context_type = agent_output.get("context_type", "unknown")
    contact_name = agent_output.get("contact_name", "")
    contact_title = agent_output.get("contact_title", "")

    user_prompt = f"""
{RUBRIC_TEXT}

═══════════════════════════════════════════════════════════
EVALUATION CONTEXT TAGS
═══════════════════════════════════════════════════════════
  Investor:         {investor}
  Format:           {fmt}
  Data Richness:    {data_richness}
  Feedback State:   {feedback_state}
  Investor Context: {investor_context}
  Context Type:     {context_type}

═══════════════════════════════════════════════════════════
INPUT DATA (CompanyBundle — what the agent had access to)
═══════════════════════════════════════════════════════════

── COMPANY CORE ──
{_fmt_company_core(company_core)}

── FOUNDERS ──
{_fmt_founders(founders)}

── KEY SIGNALS ──
{_fmt_signals(key_signals)}

── NEWS ARTICLES ──
{_fmt_news(news_articles)}

Target Contact:
  Name:  {contact_name}
  Title: {contact_title}

═══════════════════════════════════════════════════════════
AGENT OUTPUT
═══════════════════════════════════════════════════════════
Format:       {fmt}
Investor:     {investor}
Context Type: {context_type}
{"Subject: " + subject_line if subject_line else "(No subject line — LinkedIn format or parsing failure)"}

--- MESSAGE START ---
{message if message else "(EMPTY — generation failure)"}
--- MESSAGE END ---

═══════════════════════════════════════════════════════════
EVALUATION INSTRUCTIONS
═══════════════════════════════════════════════════════════

Follow these steps exactly:

STEP 1 — AUTOMATIC FLAG SCAN
Check for each of the 4 automatic flag conditions listed in the rubric.
List any that apply. This does NOT automatically fail the output — flags are for
pipeline review. Continue to scoring regardless.

STEP 2 — DIMENSION REASONING (chain of thought, one section per dimension)
For each dimension D1–D7, reason through the criteria before assigning a score.
Reference specific text from the agent output AND specific fields from the input data
to justify each claim. Be precise about what the investor is and what the expected
pattern is.

STEP 3 — SCORE ASSIGNMENT
After reasoning, assign integer scores for D1, D2, D3, D6, D7 (1–5).
Compute D4 and D5 using the deduction method described in the rubric.
If D6 is N/A (Danielle), note it and redistribute weight.

STEP 4 — COMPOSITE SCORE
Compute the weighted_score per the formula. Round to 1 decimal place.
Determine Pass (≥ 3.5) or Fail.

STEP 5 — OUTPUT JSON
Output ONLY the following JSON object (no additional text before or after):

{{
  "context_tags": {{
    "investor": "<Ashley|Tiffany|Danielle|Madison>",
    "format": "<Email|LinkedIn>",
    "data_richness": "<Rich|Moderate|Sparse>",
    "feedback_state": "<Baseline|Early|Mature>",
    "investor_context": "<None|Partial|Full>",
    "context_type": "<detected context type>"
  }},
  "flags": ["FLAG1", "FLAG2", ...],
  "reasoning": {{
    "D1_voice_fidelity": "<chain-of-thought reasoning for D1>",
    "D2_personalization_depth": "<chain-of-thought reasoning for D2>",
    "D3_context_type_appropriateness": "<chain-of-thought reasoning for D3>",
    "D4_ai_tell_absence": "<chain-of-thought reasoning for D4, list each violation found>",
    "D5_factual_accuracy": "<chain-of-thought reasoning for D5, list each violation found>",
    "D6_technical_quality": "<chain-of-thought reasoning for D6, or 'N/A — Danielle'>",
    "D7_actionability": "<chain-of-thought reasoning for D7>"
  }},
  "dimensions": {{
    "D1": {{ "score": <1-5>, "sub_scores": {{"V1": <1-5>, "V2": <1-5>, "V3": <1-5>, "V4": <1-5>, "V5": <1-5>, "V6": <1-5>}} }},
    "D2": {{ "score": <1-5>, "sub_scores": {{"P1": <1-5>, "P2": <1-5>, "P3": <1-5>, "P4": <1-5>}} }},
    "D3": {{ "score": <1-5>, "sub_scores": {{"C1": <1-5>, "C2": <1-5>, "C3": <1-5>, "C4": <1-5>, "C5": <1-5>, "C6": <1-5>, "C7": "<1-5 or N/A>"}} }},
    "D4": {{
      "score": <1.0-5.0>,
      "violations_detected": [
        {{"id": "A1", "severity": "major", "deduction": -1, "evidence": "<quoted text from message>"}},
        ...
      ]
    }},
    "D5": {{
      "score": <1.0-5.0>,
      "violations_detected": [
        {{"id": "F1", "severity": "major|minor", "deduction": -2,
          "claim": "<what the message says>", "data_check": "<what the data actually says>"}}
        ...
      ]
    }},
    "D6": {{ "score": <1-5 or "N/A">, "sub_scores": {{"T1": <1-5 or "N/A">, "T2": <1-5 or "N/A">, "T3": <1-5 or "N/A">}} }},
    "D7": {{ "score": <1-5>, "sub_scores": {{"AC1": <1-5>, "AC2": <1-5>, "AC3": <1-5>}} }}
  }},
  "weighted_score": <float, 1 decimal place, range 1.0-5.0>,
  "pass_fail": "<Pass|Fail>"
}}
""".strip()

    return SYSTEM_PROMPT, user_prompt
